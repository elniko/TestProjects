package ubpartner.logmanagement.commun;

/**
 * Class of output log XML file constant.
 */
public final class ConstanteXmlLog {

    /**
     * Empty constructor for the final class.
     */
    private ConstanteXmlLog() {
        // Class cannot be Instantiated.
    }

    /**
     * LOG4J Namespace.
     */
    public static final String NSLOG4J = "log4j";

    /**
     * Element log.
     */
    public static final String LOG = "log";

    /**
     * Attribute logger.
     */
    public static final String LOGGER = "logger";

    /**
     * Attribute timestamp.
     */
    public static final String TIMESTAMP = "timestamp";

    /**
     * Attribute thread.
     */
    public static final String THREAD = "thread";

    /**
     * Element message.
     */
    public static final String MESSAGE = "message";

    /**
     * Element event.
     */
    public static final String EVENT = "event";

    /**
     * Attribute level.
     */
    public static final String LEVEL = "level";

    /**
     * Element ReturnCode.
     */
    public static final String RETURNCODE = "ReturnCode";

    /**
     * Element ID.
     */
    public static final String ID = "ID";

}
